# Faça um programa que leia um número Inteiro e mostre na tela o seu sucessor e seu antecessor.

num = int(input('Digite um numero: '))
antecessor = num - 1
sucessor = num + 1
print('Analisando o valor de {}, seu antecessor é {} e o seu sucessor é {}'.format(num, antecessor, sucessor))

print('num é igual a 10? {}'.format(num == 10))
